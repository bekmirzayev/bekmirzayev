a=input()
k=['1','2','3','4','5','6','7','8','9','0']
k1=['-','*','+',':','/','%','^','=']
k2=['!',"'",'"',',','.','`','&','(',')','[',']','{','}',';','#','|','\'']
s=0
s1=0
s2=0
s3=0
for i in a:
 if i in k:
   s1+=1
 elif i in k2:
   s2+=1
 elif i in k1:
   s3+=1
 else:
   s+=1
l=[s,s1,s2,s3]
for i in l:
 if i!=0:
   print(i,end=' ')
